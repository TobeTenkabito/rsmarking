function interpolate(template, params = {}) {
    return String(template).replace(/\{(\w+)\}/g, (_, key) => params[key] ?? '');
}

function normalizeLanguage(language) {
    if (!language) return DEFAULT_LANGUAGE;
    const normalized = String(language).toLowerCase();
    if (normalized.startsWith('en')) return 'en';
    if (normalized.startsWith('ja')) return 'ja';
    if (normalized.startsWith('es')) return 'es';
    return 'zh';
}

function translateOperationName(value) {
    return operationNameCatalog[currentLanguage]?.[value] ?? translateLiteral(value);
}

function translatePattern(value) {
    const patterns = literalPatterns[currentLanguage] ?? [];
    for (const [pattern, replacer] of patterns) {
        const match = String(value).match(pattern);
        if (match) return replacer(match);
    }
    return value;
}

function translateLiteral(value) {
    if (!value) return value;
    const exact = literalCatalog[currentLanguage]?.[value];
    if (exact) return exact;
    const canonicalEnglish = literalCatalog.en?.[value] ?? value;
    const localized = literalCatalog[currentLanguage]?.[canonicalEnglish];
    if (localized) return localized;
    const patterned = translatePattern(canonicalEnglish);
    return patterned === canonicalEnglish ? canonicalEnglish : patterned;
}

function translateAttribute(attr, value) {
    if (!value) return value;
    const exact = attributeCatalog[currentLanguage]?.[attr]?.[value];
    if (exact) return exact;
    const canonicalEnglish = attributeCatalog.en?.[attr]?.[value]
        ?? literalCatalog.en?.[value]
        ?? value;
    return attributeCatalog[currentLanguage]?.[attr]?.[canonicalEnglish]
        ?? translateLiteral(canonicalEnglish);
}

function translateTextNode(node) {
    const original = node.__rsOriginalText ?? node.nodeValue;
    node.__rsOriginalText = original;

    const trimmed = original.trim();
    if (!trimmed) return;

    const translated = translateLiteral(trimmed);
    if (translated === trimmed) {
        node.nodeValue = original;
        return;
    }

    const leading = original.match(/^\s*/)?.[0] ?? '';
    const trailing = original.match(/\s*$/)?.[0] ?? '';
    node.nodeValue = `${leading}${translated}${trailing}`;
}

function translateElementAttribute(element, attr) {
    const originalKey = `rsOriginal${attr.charAt(0).toUpperCase()}${attr.slice(1)}`;
    const original = element.dataset[originalKey] ?? element.getAttribute(attr);
    if (!original) return;

    element.dataset[originalKey] = original;
    element.setAttribute(attr, translateAttribute(attr, original));
}

